  int result = addOne(2);
  print("결과 : ${result}");

  print("2*1=2");
  print("2*2=4");
  print("2*3=6");
  print("2*4=8");
  print("2*5=10");
  print("2*6=12");
  print("2*7=14");
  print("2*8=16");
  print("2*9=18");

  int num = 3;

  print("${num}*1=${num * 1}");
  print("${num}*2=${num * 2}");
  print("${num}*3=${num * 3}");
  print("${num}*4=${num * 4}");
  print("${num}*5=${num * 5}");
  print("${num}*6=${num * 6}");
  print("${num}*7=${num * 7}");
  print("${num}*8=${num * 8}");
  print("${num}*9=${num * 9}");
}

//산술 연산자
void main() {
  print("3+2=${3 + 2}");
  print("3-2=${3 - 2}");
  print("3*2=${3 * 2}");
  print("3/2=${3 / 2}");
  print("3%2=${3 % 2}");
  print("3~/2=${3 ~/ 2}");

  //비교 연산자
  print("2==3 -> ${2 == 3}");
  print("2<3 -> ${2 < 3}");
  print("2>3 -> ${2 > 3}");
  print("2<=3 -> ${2 <= 3}");

  //논리 연산자
  print("!true -> ${!true}");
  print("true&&true -> ${true && false}");
  print("true&&true -> ${true && true}");
  print("true||false -> ${true || false}");
}
